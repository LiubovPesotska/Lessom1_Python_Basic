
# Ваше завдання — написати програму, яка переводить число у формат часу у читальному вигляді.
#
# Користувач повинен ввести число більше або дорівнює 0 і менше ніж 8640000.
#
# Число, яке є кількістю секунд, необхідно перевести в дні, години, хвилини та секунди.
#
# Ну і додатковим завданням є турбота про виведення.
#
# Слово "день" підбирається на основі кількості днів,
# а години, хвилини і секунди повинні заповнюватися нулями при одноцифрових значеннях.
#
# Підказка: одна хвилина - 60 сек. , В одній годині 60 * 60 сек, в одній добі 24 * 60 * 60 сек.
# Тобто використовуючи функцію divmod або методи поділу // і % вам
# необхідно знайти відповідну кількість днів, годин, хвилин, а те що залишиться - це секунди, які менше 60 ;)

# Представим, что у нас есть число секунд
seconds = 12347

# Считаем количество дней
days = seconds // (24 * 3600)
# Остаток секунд после вычисления дней
seconds = seconds % (24 * 3600)

# Считаем количество часов
hours = seconds // 3600
# Остаток секунд после вычисления часов
seconds = seconds % 3600

# Считаем количество минут
minutes = seconds // 60
# Остаток секунд после вычисления минут
seconds = seconds % 60

# Определяем правильное слово для "день"
if days == 1:
    day_word = "день"
else:
    day_word = "дней"

# Форматируем строку вывода так, чтобы часы, минуты и секунды всегда были из двух цифр
current_timestamp = f"{days} {day_word}, {hours:02}:{minutes:02}:{seconds:02}"

# Печатаем результат
print(current_timestamp)

